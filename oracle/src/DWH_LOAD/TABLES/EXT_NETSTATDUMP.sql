CREATE TABLE EXT_NETSTATDUMP(
  "SNAPSHOTTIME" VARCHAR2(4000), 
	"PROTOCOL" VARCHAR2(4000), 
	"LOCALADDR" VARCHAR2(4000), 
	"FOREIGNADDR" VARCHAR2(4000), 
	"APPDETAILS" VARCHAR2(4000))
ORGANIZATION EXTERNAL(
  TYPE ORACLE_LOADER
  DEFAULT DIRECTORY "DWH_NETSTATDUMPS_DIR"
  ACCESS PARAMETERS(
    RECORDS DELIMITED BY newline
    SKIP 1
    FIELDS CSV WITH EMBEDDED TERMINATED BY ',' MISSING FIELD VALUES ARE NULL(
      SNAPSHOTTIME CHAR(4000),
      PROTOCOL CHAR(4000),
      LOCALADDR CHAR(4000),
      FOREIGNADDR CHAR(4000),
      APPDETAILS CHAR(4000)
    ))
  LOCATION('netstatdump20170514161401.csv')
)
REJECT LIMIT 1 ;